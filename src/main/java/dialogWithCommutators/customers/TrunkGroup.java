package dialogWithCommutators.customers;

public class TrunkGroup {
    private String tgName;
    private int tgNumber;
    private String csc;

    public String getTgName() {
        return tgName;
    }

    public int getTgNumber() {
        return tgNumber;
    }

    public String getCsc() {
        return csc;
    }

    public void setTgName(String tgName) {
        this.tgName = tgName;
    }

    public void setTgNumber(int tgNumber) {
        this.tgNumber = tgNumber;
    }

    public void setCsc(String csc) {
        this.csc = csc;
    }
}
